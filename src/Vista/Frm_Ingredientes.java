    private void agregar() {
        pc.getIngrediente().setNombre(txtNombre.getText());
        pc.getIngrediente().setCantidad(txtCantidad.getText());
        if (pc.addIngrediente(posicion)) {
            pc.setIngrediente(null);
            limpiar();
            JOptionPane.showMessageDialog(this, "Se inserto con exito");
        } else {
            JOptionPane.showMessageDialog(this, "Error al insertar ingrediente");
        }
    }

    private void cargarTabla() {
        tp.setLsa(pc.listaIngredientes(posicion));
        tablaIngredientes.setModel(tp);
        tablaIngredientes.getColumnModel().getColumn(0).setWidth(10);
        tablaIngredientes.updateUI();
    }

