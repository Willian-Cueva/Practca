    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int s = tablaIngredientes.getSelectedRow();
        if (s > -1) {
            int sl = JOptionPane.showConfirmDialog(this, "¿Desea eliminar el ingrediente?", "Seguro?", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (sl == 0) {
                if (pc.listaIngredientes(posicion).emilinarDpp(s)) {
                    JOptionPane.showMessageDialog(this, "Se elimino correctamente");
                } else {
                    JOptionPane.showMessageDialog(this, "No se elimino");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se selecciono ningun elemento");
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

