    private void btnIngredientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngredientesActionPerformed
        // TODO add your handling code here:
        int s = tabla.getSelectedRow();
        if (s > -1) {
            new Frm_Ingredientes(pc, s).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un postre de la lista", "NO seleccionado", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnIngredientesActionPerformed

    private void btnIngredientes1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngredientes1ActionPerformed
        // TODO add your handling code here:
        int s = tabla.getSelectedRow();
        if (s > -1) {
            if (pc.getLsa().emilinarDpp(s)) {
                JOptionPane.showMessageDialog(this, "Se elimino exitosamente");
            } else {
                JOptionPane.showMessageDialog(this, "No se elimino", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un elemento de la tabla", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        limpiar();
    }//GEN-LAST:event_btnIngredientes1ActionPerformed

