    private void agregar() {
        pc.getPostre().setNombre(txtNombre.getText());
        if (pc.addPostre()) {
            JOptionPane.showMessageDialog(this, "Se inserto el postre con exito", "Exitoso", JOptionPane.INFORMATION_MESSAGE);
            pc.setPostre(null);
            cargarTabla();
            limpiar();
        } else {
            JOptionPane.showMessageDialog(this, "Error al insertar Postre", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void cargarTabla() {
        tp.setLsa(pc.getLsa());
        tabla.setModel(tp);
        tabla.updateUI();
    }

